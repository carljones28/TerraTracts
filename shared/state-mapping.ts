/**
 * Convert state name to abbreviation
 * @param stateName Full state name (e.g., "Texas")
 * @returns State abbreviation (e.g., "TX") or the original string if not found
 */
export function getStateAbbreviation(stateName: string): string {
  return stateNameToAbbreviation[stateName] || stateName;
}

/**
 * Convert state abbreviation to full name
 * @param stateAbbr State abbreviation (e.g., "TX")
 * @returns Full state name (e.g., "Texas") or the original string if not found
 */
export function getStateName(stateAbbr: string): string {
  return stateAbbreviationToName[stateAbbr] || stateAbbr;
}

/**
 * Normalize state input to both name and abbreviation for flexible matching
 * @param state State name or abbreviation
 * @returns Object with both name and abbreviation
 */
export function normalizeState(state: string): { name: string; abbreviation: string } {
  const abbreviation = getStateAbbreviation(state);
  const name = getStateName(state);
  
  return {
    name: name === state ? getStateName(abbreviation) : name,
    abbreviation: abbreviation === state ? getStateAbbreviation(name) : abbreviation
  };
}